public class Mode {

    private String tit;
    private String masg;

    public Mode(String tit, String masg) {
        this.tit = tit;
        this.masg = masg;
    }

    public String getTit() {
        return tit;
    }

    public void setTit(String tit) {
        this.tit = tit;
    }

    public String getMasg() {
        return masg;
    }

    public void setMasg(String masg) {
        this.masg = masg;
    }
}
